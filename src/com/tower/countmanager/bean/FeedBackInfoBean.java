package com.tower.countmanager.bean;

public class FeedBackInfoBean {
	//阶段名称
	String step_name;
	//分数
	String score;
	//反馈流水号
	String feedback_sno;
	//任务说明
	String complete_desc;
	//办理时间
	String feedback_date;
	//办理人
	String operator;

	public String getStep_name() {
		return step_name;
	}

	public String getScore() {
		return score;
	}

	public String getFeedback_sno() {
		return feedback_sno;
	}

	public String getComplete_desc() {
		return complete_desc;
	}

	public String getFeedback_date() {
		return feedback_date;
	}

	public String getOperator() {
		return operator;
	}

}
